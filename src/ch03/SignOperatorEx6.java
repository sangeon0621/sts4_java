package ch03;

public class SignOperatorEx6 {

	public static void main(String[] args) {

//		대입 연산자 (+=, -=, *= .....)
		
		int result = 1;
		int result2 = 2;
		int result3 = 3;
		
		result += 10;
//		result = result + 10;
		System.out.println(result);
		
		result2 -= 10;
//		result2 = result2 - 10;
		System.out.println(result2);
		
		result3 *= 10;
//		result3 = result2 * 10;
		System.out.println(result3);
		
	}

}
